// 导出更多词汇函数
export const getComprehensiveWordsByDifficulty = (difficulty: 'easy' | 'medium' | 'hard'): Word[] => {
  return comprehensiveToeflWords.filter(word => word.difficulty === difficulty);
};

export const getComprehensiveWordsByFrequency = (minFreq: number): Word[] => {
  return comprehensiveToeflWords.filter(word => word.frequency >= minFreq);
};

export const searchComprehensiveWords = (query: string): Word[] => {
  const lowerQuery = query.toLowerCase();
  return comprehensiveToeflWords.filter(word => 
    word.word.toLowerCase().includes(lowerQuery) ||
    word.translation.includes(query) ||
    word.definition.toLowerCase().includes(lowerQuery)
  );
};