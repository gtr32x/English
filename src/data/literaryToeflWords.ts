// 导出文学艺术词汇函数
export const getLiteraryWordsByType = (type: string): Word[] => {
  const typeMap: { [key: string]: string[] } = {
    'writing': ['prose', 'narrative', 'manuscript', 'composition', 'chronicle'],
    'analysis': ['critique', 'metaphor', 'symbolism', 'imagery', 'irony'],
    'style': ['aesthetic', 'authentic', 'eloquent', 'articulate', 'ambiguous'],
    'forms': ['epic', 'allegory', 'genre']
  };
  
  const keywords = typeMap[type] || [];
  return literaryToeflWords.filter(word => 
    keywords.includes(word.word)
  );
};

export const getWritingWords = (): Word[] => {
  return getLiteraryWordsByType('writing');
};

export const getLiteraryAnalysisWords = (): Word[] => {
  return getLiteraryWordsByType('analysis');
};

export const getArtisticWords = (): Word[] => {
  const artisticKeywords = ['aesthetic', 'canvas', 'depict', 'composition'];
  return literaryToeflWords.filter(word => 
    artisticKeywords.includes(word.word)
  );
};

export const getHighFrequencyLiteraryWords = (): Word[] => {
  return literaryToeflWords.filter(word => word.frequency >= 3);
};