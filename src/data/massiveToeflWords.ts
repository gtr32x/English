export const getWordsByFrequency = (minFreq: number): Word[] => {
  return massiveToeflWords.filter(word => word.frequency >= minFreq);
};

export const getWordsByLength = (minLength: number, maxLength: number): Word[] => {
  return massiveToeflWords.filter(word => 
    word.word.length >= minLength && word.word.length <= maxLength
  );
};

export const getRandomWordsByDifficulty = (difficulty: 'easy' | 'medium' | 'hard', count: number): Word[] => {
  const filtered = massiveToeflWords.filter(word => word.difficulty === difficulty);
  const shuffled = [...filtered].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};