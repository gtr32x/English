// 导出环境地理词汇函数
export const getEnvironmentalWordsByCategory = (category: string): Word[] => {
  const categoryMap: { [key: string]: string[] } = {
    'climate': ['climate', 'atmosphere', 'emission', 'drought', 'hurricane'],
    'conservation': ['conservation', 'biodiversity', 'habitat', 'sustainable', 'renewable'],
    'pollution': ['pollution', 'contamination', 'deforestation', 'erosion'],
    'geography': ['landscape', 'glacier', 'vegetation', 'irrigation', 'fossil'],
    'wildlife': ['extinction', 'habitat', 'biodiversity', 'ecosystem']
  };
  
  const keywords = categoryMap[category] || [];
  return environmentalToeflWords.filter(word => 
    keywords.includes(word.word)
  );
};

export const getClimateWords = (): Word[] => {
  return getEnvironmentalWordsByCategory('climate');
};

export const getConservationWords = (): Word[] => {
  return getEnvironmentalWordsByCategory('conservation');
};

export const getPollutionWords = (): Word[] => {
  return getEnvironmentalWordsByCategory('pollution');
};

export const getGeographyWords = (): Word[] => {
  return getEnvironmentalWordsByCategory('geography');
};

export const getHighFrequencyEnvironmentalWords = (): Word[] => {
  return environmentalToeflWords.filter(word => word.frequency >= 4);
};

export const getSustainabilityWords = (): Word[] => {
  const sustainabilityKeywords = ['sustainable', 'renewable', 'conservation', 'biodiversity'];
  return environmentalToeflWords.filter(word => 
    sustainabilityKeywords.includes(word.word)
  );
};