// 导出学术词汇函数
export const getAcademicWordsByLevel = (level: string): Word[] => {
  const levelMap: { [key: string]: string } = {
    'undergraduate': 'medium',
    'graduate': 'hard',
    'basic': 'easy'
  };
  
  const targetDifficulty = levelMap[level] || 'medium';
  return academicToeflWords.filter(word => word.difficulty === targetDifficulty);
};

export const getHighFrequencyAcademicWords = (): Word[] => {
  return academicToeflWords.filter(word => word.frequency >= 4);
};

export const getResearchMethodologyWords = (): Word[] => {
  const methodologyKeywords = ['analyze', 'evaluate', 'demonstrate', 'establish', 'criteria'];
  return academicToeflWords.filter(word => 
    methodologyKeywords.includes(word.word)
  );
};

export const getConceptualWords = (): Word[] => {
  const conceptualKeywords = ['abstract', 'framework', 'dimension', 'fundamental'];
  return academicToeflWords.filter(word => 
    conceptualKeywords.includes(word.word)
  );
};