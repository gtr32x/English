// 导出商业词汇函数
export const getBusinessWordsByCategory = (category: string): Word[] => {
  const categoryMap: { [key: string]: string[] } = {
    'finance': ['finance', 'budget', 'investment', 'asset', 'liability', 'currency'],
    'management': ['allocate', 'efficient', 'enterprise', 'incentive'],
    'trade': ['export', 'import', 'commodity', 'consumer'],
    'economics': ['economy', 'deficit', 'fluctuate', 'generate']
  };
  
  const keywords = categoryMap[category] || [];
  return businessToeflWords.filter(word => 
    keywords.includes(word.word)
  );
};

export const getFinanceWords = (): Word[] => {
  return getBusinessWordsByCategory('finance');
};

export const getManagementWords = (): Word[] => {
  return getBusinessWordsByCategory('management');
};

export const getHighFrequencyBusinessWords = (): Word[] => {
  return businessToeflWords.filter(word => word.frequency >= 4);
};